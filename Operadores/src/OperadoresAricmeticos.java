public class OperadoresAricmeticos {
    public static void main(String[] args) {
        System.out.println("*** Operadores Aricmeticos ***");

        double a = 5, b = 3 , resultados;

        //Suma +
        resultados = a + b;
        System.out.println("resultados Suma = " + resultados);

        //Resta -
        resultados = a - b;
        System.out.println("resultados Resta = " + resultados);

        //Multiplicacion *
        resultados = a * b;
        System.out.println("resultados Multiplicacion = " + resultados);

        //Division /
        resultados = a / b;
        System.out.println("resultados Division = " + resultados);

        //Residuo de division /
        resultados = a % b;
        System.out.println("resultados modeulo = " + resultados);
    }
}
